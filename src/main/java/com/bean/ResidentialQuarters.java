package com.bean;

public class ResidentialQuarters {
    String id;
    String developerId;
    String residentialQuartersName;
    String state;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDeveloperId() {
        return developerId;
    }

    public void setDeveloperId(String developerId) {
        this.developerId = developerId;
    }

    public String getResidentialQuartersName() {
        return residentialQuartersName;
    }

    public void setResidentialQuartersName(String residentialQuartersName) {
        this.residentialQuartersName = residentialQuartersName;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }
}
